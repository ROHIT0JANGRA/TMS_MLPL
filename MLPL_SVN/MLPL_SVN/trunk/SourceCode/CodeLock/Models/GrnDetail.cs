using System;
using System.ComponentModel.DataAnnotations;

namespace CodeLock.Models
{
  public class GrnDetail
  {
    public GrnDetail()
    {
      this.InspectionStatus = false;
    }

    public byte CompanyId { get; set; }

    public short WarehouseId { get; set; }

    public long GrnId { get; set; }

    public string GrnNo { get; set; }

    public DateTime GrnDate { get; set; }

    [DisplayFormat(ConvertEmptyStringToNull = false)]
    [Display(Name = "Product")]
    public string ProductCode { get; set; }

    public int ProductId { get; set; }

    public string ProductName { get; set; }

    public string Uom { get; set; }

    public Decimal UomQuantity { get; set; }

    public int Packages { get; set; }

    public Decimal Quantity { get; set; }

    public Decimal GoodQuantity { get; set; }

    public Decimal ShortQuantity { get; set; }

    public Decimal DocumentQuantity { get; set; }

    public Decimal UnitPrice { get; set; }

    public Decimal? Amount { get; set; }

    public Decimal? ShortExcess { get; set; }

    public bool IsChecked { get; set; }

    public string FirstSerialNo { get; set; }

    public string SecondSerialNo { get; set; }

    public short? BinId { get; set; }

    public long? PutAwayId { get; set; }

    public bool? IsSingle { get; set; }

    public Decimal InspectionQuantity { get; set; }

    [Display(Name = "Inspection Status")]
    public bool InspectionStatus { get; set; }

    public bool OnHold { get; set; }

    public string SkuCode { get; set; }

    public string SkuName { get; set; }

    public int SkuId { get; set; }

    public string SkuDescription { get; set; }

    public bool IsSerialNumber { get; set; }

    public string BatchNumber { get; set; }
  }
}
