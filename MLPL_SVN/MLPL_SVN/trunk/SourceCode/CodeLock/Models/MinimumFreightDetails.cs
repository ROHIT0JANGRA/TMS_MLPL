      this.MinimumFreightRate = new Decimal(0);
      this.MinimumFreightRateType = "";
      this.MinimumFreightAmount = new Decimal(0);
      this.MinimumFreightLowerLimit = new Decimal(0);
      this.MinimumFreightUpperLimit = new Decimal(0);
